-- TẠO BẢNG VÀ KHÓA CHÍNH
GO
CREATE TABLE KHOA
(
	MAKHOA CHAR(4),
	TENKHOA NVARCHAR(50),
	NAMTL INT,
	PHONG CHAR(3),
	DIENTHOAI CHAR(13),
	TRUONGKHOA CHAR(3),
	NGAYNHANCHUC DATE

	
	CONSTRAINT PK_KHOA 
	PRIMARY KEY(MAKHOA) 
)

CREATE TABLE BOMON
(
	MABM NVARCHAR(5),
	TENBM NVARCHAR(50),
	PHONG CHAR(3),
	DIENTHOAI CHAR(13),
	TRUONGBM CHAR(3),
	MAKHOA CHAR(4),
	NGAYNHANCHUC DATE

	CONSTRAINT PK_BOMON
	PRIMARY KEY(MABM)
)

CREATE TABLE GIAOVIEN
(
	MAGV CHAR(3),
	HOTEN NVARCHAR(30),
	LUONG INT, 
	PHAI NVARCHAR(5),
	NGSINH DATE,
	DIACHI NVARCHAR(100) NOT NULL,
	GVQLCM CHAR(3), 
	MABM NVARCHAR(5)

	CONSTRAINT PK_GV
	PRIMARY KEY(MAGV)

)

-- TẠO RÀNG BUỘC MIỀN GIÁ TRỊ CHO BẢNG GIÁO VIÊN Ở THUỘC TÍNH PHÁI
ALTER TABLE GIAOVIEN
ADD 
	CONSTRAINT CK_GV_PHAI
	CHECK (PHAI = 'Nam' OR PHAI = N'Nữ')


CREATE TABLE GV_DT
(
	MAGV CHAR(3),
	DIENTHOAI CHAR(13)

	CONSTRAINT PK_GVDT
	PRIMARY KEY(MAGV, DIENTHOAI)
)

CREATE TABLE NGUOITHAN
(
	MAGV CHAR(3),
	TEN NVARCHAR(50),
	NGSINH DATE,
	PHAI NVARCHAR(5)

	CONSTRAINT PK_NGUOITHAN
	PRIMARY KEY(MAGV, TEN)
)

ALTER TABLE NGUOITHAN
ADD
	CONSTRAINT CK_PHAI_NT
	CHECK (PHAI = 'NAM' OR PHAI = N'NỮ')

CREATE TABLE CHUDE
(
	MACD NVARCHAR(5),
	TENCD NVARCHAR(50)

	CONSTRAINT PK_CHUDE
	PRIMARY KEY(MACD)
)

CREATE TABLE DETAI
(
	MADT CHAR(3),
	TENDT NVARCHAR(50),
	CAPQL NVARCHAR(50),
	KINHPHI FLOAT,
	NGAYBD DATE,
	NGAYKT DATE,
	MACD NVARCHAR(5),
	GVCNDT CHAR(3)

	CONSTRAINT PK_DETAI
	PRIMARY KEY(MADT)
)

CREATE TABLE CONGVIEC
(
	MADT CHAR(3),
	SOTT INT,
	TENCV NVARCHAR(100),
	NGAYBD DATE,
	NGAYKT DATE

	CONSTRAINT PK_CONGVIEC
	PRIMARY KEY(MADT, SOTT)
)

CREATE TABLE THAMGIADT
(
	MAGV CHAR(3),
	MADT CHAR(3),
	STT INT,
	PHUCAP FLOAT,
	KETQUA NVARCHAR(6)

	CONSTRAINT PK_THAMGIADT
	PRIMARY KEY(MAGV, MADT, STT)
)

GO

-- TẠO KHÓA NGOẠI

-- FK_GVDT -> GV

ALTER TABLE GV_DT
ADD 
	CONSTRAINT FK_GVDT_GV
	FOREIGN KEY(MAGV)
	REFERENCES GIAOVIEN

-- FK_NGUOI THAN -> GV

ALTER TABLE NGUOITHAN
ADD
	CONSTRAINT FK_NGUOITHAN
	FOREIGN KEY(MAGV)
	REFERENCES GIAOVIEN

-- FK_DETAI_CD -> CHUDE
-- FK_DETAI_GV -> GIAOVIEN
ALTER TABLE DETAI
ADD
	CONSTRAINT FK_DETAI_CD
	FOREIGN KEY(MACD)
	REFERENCES CHUDE,

	CONSTRAINT FK_DETAI_GV
	FOREIGN KEY(GVCNDT)
	REFERENCES GIAOVIEN

-- FK_CONGVIEC -> DETAI

ALTER TABLE CONGVIEC
ADD
	CONSTRAINT FK_CONGVIEC
	FOREIGN KEY(MADT)
	REFERENCES DETAI

-- FK_TGDT_CV -> CONGVIEC
-- FK_TGDT_GV -> GV

ALTER TABLE THAMGIADT
ADD
	CONSTRAINT FK_TGDT_CV
	FOREIGN KEY(MADT, STT)
	REFERENCES CONGVIEC,

	CONSTRAINT FK_TGDT_GV
	FOREIGN KEY(MAGV)
	REFERENCES GIAOVIEN

-- FK_KHOA -> GIAOVIEN

ALTER TABLE KHOA
ADD
	CONSTRAINT FK_KHOA
	FOREIGN KEY(TRUONGKHOA)
	REFERENCES GIAOVIEN

-- FK_GV_GV
-- FK_GV_BOMON
ALTER TABLE GIAOVIEN
ADD
	CONSTRAINT FK_GV_GV
	FOREIGN KEY(GVQLCM)
	REFERENCES GIAOVIEN, 

	CONSTRAINT FK_GV_BOMON
	FOREIGN KEY(MABM)
	REFERENCES BOMON


-- FK_BM_KHOA -> KHOA
-- FK_BM_GV -> GIAOVIEN
ALTER TABLE BOMON
ADD
	CONSTRAINT FK_BM_K
	FOREIGN KEY(MAKHOA)
	REFERENCES KHOA, 

	CONSTRAINT FK_BM_GV
	FOREIGN KEY(TRUONGBM)
	REFERENCES GIAOVIEN


